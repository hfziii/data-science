"""# 2.   Periksa tipe data pada setiap kolom"""

# Menampilkan tipe data pada setiap kolom
print('Tipe data pada setiap kolom:')
print(df.dtypes)

"""# 3.   Identifikasi dan tangani jika ada data yang hilang atau tidak valid."""

# Menampilkan jumlah data yang hilang pada setiap kolom
print('Jumlah data yang hilang pada setiap kolom:')
print(df.isnull().sum())

#hapus baris yang mempunyai data tidak valid
df.dropna(inplace=True)
#hapus kolom yang mempunyai data tidak valid
df.dropna(axis=1, inplace=True)

"""# **LANGKAH 3 : ANALISIS DATA**

# 1. Hitung total data untuk setiap produk
"""

# Hitung total data untuk setiap universitas
total_universitas = df.groupby('institution').sum()
print('Total data untuk setiap produk:')
print(total_universitas)

"""# 2. Hitung total data per bulan/tahun"""

# Hitung total data per tahun
df['tahun'] = pd.to_datetime(df['year']).dt.year
total_tahun = df.groupby('year').size()
print('Total data per tahun')
print(total_tahun)

"""# 3. Hitung rata-rata data"""

# Hitung rata-rata data
rata_rata = df.mean()
print('Rata-rata data:')
print(rata_rata)

"""# 4. Temukan produk dengan harga tertinggi dan terendah"""

# Temukan produk dengan score tertinggi dan terendah
score_tertinggi = df.loc[df['score'].idxmax()]
score_terendah = df.loc[df['score'].idxmin()]
print('Universitas dengan score tertinggi:')
print(score_tertinggi)
print('Universitas dengan score terendah:')
print(score_terendah)

"""# 5. Temukan bulan dengan total data tertinggi dan terendah."""

# Temukan tahun dengan total data tertinggi dan terendah
tahun_tertinggi = total_tahun.idxmax()
tahun_terendah = total_tahun.idxmin()
print('Tahun dengan total data tertinggi:', tahun_tertinggi)
print('Tahun dengan total data terendah:', tahun_terendah)

