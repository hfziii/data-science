df = pd.read_csv('cwurData.csv')
df.head()

"""# **LANGKAH 2 : MEMAHAMI DATA**

# 1.   Identifikasi jumlah baris dan kolom
"""

# Menampilkan jumlah baris dan kolom
print('Jumlah baris:', df.shape[0])
